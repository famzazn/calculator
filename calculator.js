const add = function(x, y) {
    return x + y;
}

const subtract = function(x, y) {
    return x - y;
}

const multiply = function(x, y) {
    return x * y;
}

const divide = function(x, y) {
    return x / y;
}

console.log(add(5, 7));
console.log(subtract(5, 7));
console.log(multiply(5, 7));
console.log(divide(5, 7));
